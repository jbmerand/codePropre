package ex1;

/**
 * Classe qui réalise des opérations arithmétiques
 * 
 * @author DIGINAMIC
 */
public class Calcul {

	/**
	 * Calcule et retourne la somme de a et b
	 * 
	 * @param a
	 *            (nombre a)
	 * @param b
	 *            (nombre b)
	 * @return int
	 */
	public int additionner(int a, int b) {
		return a + b;
	}

	/**
	 * Calcule et retourne la différence de a par b
	 * 
	 * @param a
	 *            (nombre a)
	 * @param b
	 *            (nombre b)
	 * @return
	 */
	public int soustraire(int a, int b) {
		return a - b;
	}
}
